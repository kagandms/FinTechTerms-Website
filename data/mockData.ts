/**
 * Helper to get terms by category
 */
export const getTermsByCategory = (category: Term['category']): Term[] => {
    return mockTerms.filter(term => term.category === category);
};

/**
 * Helper to search terms across all languages
 */
export const searchTerms = (query: string): Term[] => {
    const lowerQuery = query.toLowerCase().trim();
    if (!lowerQuery) return [];

    return mockTerms.filter(term =>
        term.term_en.toLowerCase().includes(lowerQuery) ||
        term.term_ru.toLowerCase().includes(lowerQuery) ||
        term.term_tr.toLowerCase().includes(lowerQuery) ||
        term.definition_en.toLowerCase().includes(lowerQuery) ||
        term.definition_ru.toLowerCase().includes(lowerQuery) ||
        term.definition_tr.toLowerCase().includes(lowerQuery)
    );
};
